// for loop(Entry controlled)
for (let i = 1; i <= 10; i++) {
  //   console.log(i);
}

// while loop
let i = 1;
while (i <= 10) {
  //   console.log(i);
  i++;
}
console.log(i);

// do-while loop
let j = 11;
do {
  console.log(j);
  j++;
} while (j <= 10);
